package bean;

public class user {
	String id;
	String username;
	String password;
	String pubKey;
	String enMess;
	String md;
	String rsa;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getPubKey() {
		return pubKey;
	}
	public void setPubKey(String pubKey) {
		this.pubKey = pubKey;
	}
	public String getEnMess() {
		return enMess;
	}
	public void setEnMess(String enMess) {
		this.enMess = enMess;
	}
	
	public String getMd() {
		return md;
	}
	public void setMd(String md) {
		this.md = md;
	}
	public String getRsa() {
		return rsa;
	}
	public void setRsa(String rsa) {
		this.rsa = rsa;
	}
	
}
